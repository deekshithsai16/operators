#And operator

num_1=15
num_2=90
print("num_1 value is : ",num_1)
print("num_2 value is : ",num_2)
print("binary form of num_1 is : ",bin(num_1))
print("binary form of num_2 is : ",bin(num_2))
res=num_1 & num_2
print(res)

#Or operator

num_1=300
num_2=909
print("num_1 value is : ",num_1)
print("num_2 value is : ",num_2)
res=num_1 | num_2
print(res)

#Xor operator


num_1=15
num_2=90
print("num_1 value is : ",num_1)
print("num_2 value is : ",num_2)
res=num_1 ^ num_2
print(res)

#Not operator

num_1=70
